# Em qualquer pasta, para instalar o Gulp no sistema e 
# poder utilizá-lo por linha de comando.
$ npm install --global gulp
# Na pasta raiz do tema, inicializamos o projeto npm, que 
# cria um arquivo `package.json` com os dados do projeto.
$ npm init
# Agora instalamos alguns dos pacotes necessários.
$ npm install --save-dev gulp gulp-less gulp-utils gulp-eslint


